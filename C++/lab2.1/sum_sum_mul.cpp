#include<iostream>
using namespace std;
int main()
{
	int x,y,sum,sub,mul;
	cout<<"Enter the value of x and y:";
	cin>>x >>y;
	sum= x+y;
	sub= x-y;
	mul=x*y;
	cout<<"The sum of x & y is:"<<sum <<endl;
	cout<<"The difference of x & y is:"<<sub <<endl;
	cout<<"The product of x & y is:"<<mul <<endl;
	return 0;
	
}
